age = 65

if age < 2:
    print("That person is a baby!")
elif age < 4:
    print("That person is a toddler!")
elif age < 13:
    print("That person is a kid!")
elif age < 20:
    print("That person is a teenager!")
elif age < 65:
    print("That person is an adult!")
else:
    print("That person is an elder!")